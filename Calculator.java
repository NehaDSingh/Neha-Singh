
public class Calculator {

	public double addition(int n1,double n2)
	{
		return n1+n2;
	}
	

	public double subtraction(int n1,double n2)
	{
		return n1-n2;
	}
	
	public double multiplication(int n1,double n2)
	{
		return n1*n2;
	}
	
	public double division(int n1,double n2)
	{
		return n1/n2;
	}
	
}
